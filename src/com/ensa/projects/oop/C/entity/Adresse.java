package com.ensa.projects.oop.C.entity;

public class Adresse {
    String num;
    String rue;
    String ville;
    String pays;

    public void setNum(String num) {
        this.num = num;
    }

    public void setRue(String rue) {
        this.rue = rue;
    }

    public void setVille(String ville) {
        this.ville = ville;
    }

    public void setPays(String pays) {
        this.pays = pays;
    }
}
